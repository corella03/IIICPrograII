    public void agregar(){
        DefaultTableModel temp = (DefaultTableModel)TablaProcesos.getModel();
        Object nuevo [] = {temp.getRowCount() + 1, "",""};
        temp.addRow(nuevo);
    }
    public void eliminar(){
        try {
            DefaultTableModel temp = (DefaultTableModel)TablaProcesos.getModel();
            temp.removeRow(temp.getRowCount()-1);
        } catch (ArrayIndexOutOfBoundsException e) {
        }
    }
    public void buscar(){
        String ele = txtElemento.getText();
        for (int i = 0; i < TablaProcesos.getRowCount(); i++) {
            if(TablaProcesos.getValueAt(i, 2).equals(ele)){
                TablaProcesos.changeSelection(i, 1, false, false);
                break;
            }
        }
    }
    public void elementoSeleccionado(){
        TableModel tablaModelo;
        tablaModelo = (TableModel)TablaProcesos.getModel();
        boolean avanzar = true;
        int registro = TablaProcesos.getSelectedRow();
        int columna = TablaProcesos.getSelectedColumn();
        if(registro == -1){
            avanzar = false;
        }else if(columna == -1){
            avanzar =  false;
        }
        if(avanzar){
            String strResultado = tablaModelo.getValueAt(TablaProcesos.getSelectedRow(), 
                    TablaProcesos.getSelectedColumn()).toString();
            JOptionPane.showMessageDialog(null, "Dato Seleccionado: " + strResultado);
        }else{
            JOptionPane.showMessageDialog(null, "No se ha seleccionado un Registro");
        }
    }
